async function run() {
  const admin = await db('users').where('role', 'admin').select('id').first();
  const createdBy = admin?.id ?? 1;

  for (const post of POSTS) {
    const master = await db('masters').where('full_name', post.author).select('id').first();
    const payload = {
      title: post.title,
      excerpt: post.excerpt,
      content: post.content,
      author_id: master?.id ?? null,
      category: 'Услуги',
      read_time: post.read_time,
      image: post.image,
      published_at: new Date(post.published_at),
      created_by: createdBy,
    };

    const existing = await db('blog_posts')
      .where({ title: post.title, category: 'Услуги' })
      .select('id')
      .first();

    if (existing?.id) {
      await db('blog_posts').where({ id: existing.id }).update(payload);
      console.log(`updated: ${post.title}`);
    } else {
      await db('blog_posts').insert(payload);
      console.log(`inserted: ${post.title}`);
    }
  }
}

run()
  .then(async () => {
    const countRow = await db('blog_posts').where('category', 'Услуги').count({ count: '*' }).first();
    console.log(`services posts total: ${countRow?.count ?? 0}`);
    await db.destroy();
  })
  .catch(async (err) => {
    console.error(err);
    await db.destroy();
    process.exit(1);
  });
